import tomllib

from . import PROJECT_ROOT, BBLOCK, BLOCK_WIDTH, BLOCK_HEIGHT
from builder.font import GlyphBuilder

# returns names of glyphs not file paths
def parse_block(x: str):
    '''Transforms a string into list of base glyph names'''

    TRUE = '#'
    LINE = '||'

    # make sure the input is 10 characters long
    assert len(x) == 10

    top = x[:3]
    mid = x[4:6]
    bot = x[7:10]

    blocks = []
    if x[0] == TRUE:
        blocks.append(1)
    if x[1] == TRUE:
        blocks.append(2)
    if x[2] == TRUE:
        blocks.append(3)

    if x[4:6] == LINE:
        # prepend so they are in order
        blocks.insert(0, 0)
    else:
        if x[4] == TRUE:
            blocks.append(4)
        if x[5] == TRUE:
            blocks.append(5)

    if x[7] == TRUE:
        blocks.append(6)
    if x[8] == TRUE:
        blocks.append(7)
    if x[9] == TRUE:
        blocks.append(8)

    return [ BBLOCK + str(x) for x in blocks ]

def glyph_defaults(glyph: GlyphBuilder):
    '''Sets defaults for each glyph, run with GlyphBuilder.do(..)'''
    glyph.set_unlink_overlap_on_save() \
        .comment('AUTOGENERATED DO NOT EDIT')

    glyph.glyph.removeOverlap()

def _convert_glyph(font, glyph_char, glyph_data):
    # allow creating a char from name
    if len(glyph_char) == 1:
        g = font.glyph().char(glyph_char)
    else:
        g = font.glyph().name(glyph_char)

    g.clear()

    width = glyph_data.get("width")
    if width:
        g.width(BLOCK_WIDTH * glyph_data["width"])

    width = glyph_data.get("width")
    if width:
        g.width(BLOCK_WIDTH * glyph_data["width"])

    block = glyph_data.get("block")
    if block:
        if isinstance(block, str):
            g.refs(parse_block(block))
        elif isinstance(block, list):
            # TODO this is barely readable
            for x in block:
                g.refs(parse_block(x[0]))
                g.transform(psMat.translate(
                    BLOCK_WIDTH * x[1][0],
                    BLOCK_HEIGHT * x[1][1]
                ))
        else:
            raise RuntimeError(f"Invalid glyph block '{block}'")

    g.do(glyph_defaults)

class Config:
    def __init__(self, data):
        self.suffix = data.get("suffix", "")
        self.description = data.get("description", "")
        self.formats = data.get("formats", ["ttf"])

        options = data.get("options", {})
        self.options = {}
        self.options["line_separator"] = options.get("line_separator", False)
        self.options["separate_words"] = options.get("separate_words", False)

        self.glyph = {}
        glyph = data.get("glyph", {})
        for k, v in glyph.items():
            self.glyph[k] = {}
            self.glyph[k]["width"] = v.get("width", 1)
            self.glyph[k]["block"] = v.get("block", None)

        # basically make aliases clones of the thing
        alias = data.get("alias", {})
        for k, v in alias.items():
            # zerowidth makes these glyphs empty with width of zero
            # skip multi character entries as they are special
            if len(k) > 1:
                continue
            for k2 in v:
                self.glyph[k2] = self.glyph[k]

        for i in alias.get("zerowidth", ""):
            self.glyph[i] = { "width": 0 }

        # automatically set lowercase letters
        for i in "abcdefghijklmnopqrstuvwxyz":
            self.glyph[i] = self.glyph[i.upper()]

    def create_glyphs(self, font):
        """Creates defined glyphs in font"""
        for glyph_ch, glyph in self.glyph.items():
            _convert_glyph(font, glyph_ch, glyph)

    @classmethod
    def load(cls, fp):
        data = tomllib.load(fp)
        return cls(data)
