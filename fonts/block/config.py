# returns names of glyphs not file paths
def block(x: str, translate=None):
    '''Transforms a string into list of base glyph names'''
    blocks = []

    top = x[:3]
    middle = x[4:6]
    bottom = x[7:]

    for i in range(3):
        if top[i] != ' ':
            blocks.append(1 + i)

    if middle == '||':
        blocks.append(0)
    else:
        for i in range(2):
            if middle[i] != ' ':
                blocks.append(4 + i)

    for i in range(3):
        if bottom[i] != ' ':
            blocks.append(6 + i)

    return [ g.BBLOCK + str(x) for x in blocks ]

